DATAS SEGMENT
    STRR DB 'HELLO WORLD!$'          ;待处理字符串
     LEN EQU $-STRR                  ;字符串中包含的字符个数
DATAS ENDS
STACKS SEGMENT
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
MOV AX,DATAS
MOV DS,AX

MOV BX,OFFSET STRR    ;设置地址指针
MOV CX,LEN            ;设置循环次数

CHECK:MOV AL,[BX]
CMP AL,'A'
JB NEXT
CMP AL,'Z'
JA NEXT
ADD AL,20H
MOV [BX],AL

NEXT:
INC BX
DEC CX
JNZ CHECK


;字符串输出
MOV DX,OFFSET STRR
MOV AH,9
INT 21H



MOV AH,4CH
INT 21H

CODES ENDS
END START
